    /**
     * Render widget output on the frontend.
     *
     * Used to generate the final HTML displayed on the frontend.
     *
     * Note that if skin is selected, it will be rendered by the skin itself,
     * not the widget.
     *
     * @since 1.0.0
     * @access public
     */
    protected function render() {
        $settings = $this->get_settings_for_display();

        ?>

    <?php if ( $settings['design_style'] === 'style_2' ): ?>

    <?php else: ?>

    <section class="nutrition-promo-section section-padding">
        <div class="container">
            <div class="row">
                <div class="col col-xs-12">
                    <div class="promo-grids">
                        <?php foreach ( $settings['slides'] as $key => $slide ) :
                            if ( !empty($slide['image']['id']) ) {
                                $image = wp_get_attachment_image_url( $slide['image']['id'], $settings['thumbnail_size'] );
                            }
                        ?>
                        <div class="grid">

                            <?php if(!empty( $image )) : ?>
                            <div class="img-holder">
                                <img src="<?php echo esc_url($image) ?>" alt>
                            </div>
                            <?php endif; ?>

                            <div class="details">

                                <?php if(!empty( $slide['title'] )) : ?>
                                <h4><?php echo elh_element_kses_basic( $slide['title'] ); ?></h4>
                                <?php endif; ?>

                                <?php if(!empty( $slide['button_link']['url'] )) : ?>
                                <a href="<?php echo esc_url($slide['button_link']['url']); ?>" class="shop-btn">
                                    <?php echo elh_element_kses_basic( $slide['button_text'] ); ?></a>
                                <?php endif; ?>
                            </div>
                        </div>
                        <?php endforeach; ?>
                    </div>
                </div>
            </div>
        </div> <!-- end container -->
    </section>
    <!-- end features-section -->

        <?php endif; ?>

        <?php
    }

}
